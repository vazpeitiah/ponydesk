    private void seleccionar(String clave){
        String cantidad_text = jTextField2.getText();
        if(Validar.isFloat(cantidad_text)){
            float cantidad = Float.parseFloat(cantidad_text);
            if(cantidad > 0){
               Articulo articulo = ArticuloController.findByClave(clave);
               if(articulo.isGranel()){
                    boolean resultado = JFSalida.agregarArticuloConClave(clave, cantidad);
                    if(resultado)
                    {
                        JFSalida.actualizarDatos();
                        this.dispose();
                    }
               }else{
                   if(cantidad == Math.round(cantidad)){
                       boolean resultado = JFSalida.agregarArticuloConClave(clave, cantidad);
                       if(resultado)
                        {
                            JFSalida.actualizarDatos();
                            this.dispose();
                        }
                   }else{
                       JOptionPane.showMessageDialog(null, 
                        "Este artículo no puede salir por fracciones", 
                        "Artículo sin granel", 
                        JOptionPane.ERROR_MESSAGE);
                   }
               }
            }else{
                JOptionPane.showMessageDialog(null, 
                    "La cantidad no puede ser 0", 
                    "Cantidad igual a 0", 
                    JOptionPane.ERROR_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null, 
                    "Cantidad inválida. Debe ingresar un número positivo entero o con decimales", 
                    "Error al ingresar la cantidad", 
                    JOptionPane.ERROR_MESSAGE);
        }
    }

