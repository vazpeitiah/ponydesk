    private void txtBuscarKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscarKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (txtBuscar.getText().isEmpty() == false) {
                actualizarTabla(txtBuscar.getText());
                if (jTable1.getRowCount() == 0) {
                    JOptionPane.showMessageDialog(null, "No se encontraron artículos con ese nombre");
                    actualizarTabla("");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Debes escribir el nombre del artículo en el campo de ");
            }
        }
    }//GEN-LAST:event_txtBuscarKeyPressed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (jTable1.getSelectedRowCount() > 0) {
            int row = jTable1.getSelectedRow();
            String clave = (String) jTable1.getValueAt(row, 0);
            seleccionar(clave);
        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un artículo en la parte superior", "Artículo no seleccionado", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

