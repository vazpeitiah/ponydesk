    private void txtBuscarKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscarKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (!txtBuscar.getText().equals("")) {
                String keywords = txtBuscar.getText();
                actualizarTablaArticulos(keywords);
                if (tableArticulos.getRowCount() <= 0) {
                    JOptionPane.showMessageDialog(null, "No se encontraron resultados de \"" + txtBuscar.getText() + "\".");
                    actualizarTablaArticulos("");
                } else {
                    if (ArticuloController.findByClave(keywords) != null) {
                        txtBuscar.setText("");
                    }
                }
            }
        }
    }//GEN-LAST:event_txtBuscarKeyPressed

    private void tableArticulosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableArticulosMouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 2 && tableArticulos.getSelectedRowCount() > 0) {
            Point point = evt.getPoint();
            int row = tableArticulos.rowAtPoint(point);
            String clave = (String) tableArticulos.getValueAt(row, 0);
            Articulo art = new ArticuloController().findByClave(clave);
            JDFormularioArticulo editForm = new JDFormularioArticulo(null, true, art);
            editForm.setVisible(true);
            
        }
    }//GEN-LAST:event_tableArticulosMouseClicked


