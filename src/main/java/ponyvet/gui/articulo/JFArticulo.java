    private void btnLotesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLotesActionPerformed
        // TODO add your handling code here:
         if (tableArticulos.getSelectedRowCount() > 0) {        
             int row = tableArticulos.getSelectedRow();
             String clave = (String) tableArticulos.getValueAt(row, 0);
             Articulo art = new ArticuloController().findByClave(clave);
             
             if(art.isLote()){
                 JDVerLotes lotes = new JDVerLotes(new JFrame(), true, clave);
                 lotes.setVisible(true);
             }else{
                 JOptionPane.showMessageDialog(null, "El artículo seleccionado no tiene lotes relacionados", "Artículo sin lotes", JOptionPane.INFORMATION_MESSAGE);
             }
            
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione un artículo de la tabla", "Artículo no seleccionado", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnLotesActionPerformed

