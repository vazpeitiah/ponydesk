    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        if(tableArticulos.getSelectedRowCount() > 0){
            int row = tableArticulos.getSelectedRow();
            String clave = (String) tableArticulos.getValueAt(row, 0);
            Articulo art = new ArticuloController().findByClave(clave);
            JDFormularioArticulo editForm = new JDFormularioArticulo(null, true, art);
            editForm.setVisible(true);
        }else{
            JOptionPane.showMessageDialog(null, "Seleccione un artículo de la tabla", "Artículo no seleccionado", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        if (tableArticulos.getSelectedRowCount() > 0) {
            int resp = JOptionPane.showConfirmDialog(null, "¿Deseas borrar el registro?", "Eliminar registro", JOptionPane.YES_NO_OPTION, JOptionPane.ERROR_MESSAGE);

            if (resp == 0) {
                int row = tableArticulos.getSelectedRow();
                String clave = (String) tableArticulos.getValueAt(row, 0);
                Articulo art = new ArticuloController().findByClave(clave);

                ArticuloController.delete(art);

                

                JFArticulo.actualizarTablaArticulos("");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione un artículo de la tabla", "Artículo no seleccionado", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

