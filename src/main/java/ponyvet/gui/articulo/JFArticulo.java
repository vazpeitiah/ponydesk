    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        if (!txtBuscar.getText().equals("")) {
            String keywords = txtBuscar.getText();
            actualizarTablaArticulos(keywords);
            if (tableArticulos.getRowCount() <= 0) {
                JOptionPane.showMessageDialog(null, "No se encontraron resultados de \"" + txtBuscar.getText() + "\".");
                actualizarTablaArticulos("");
            }else{
                if(ArticuloController.findByClave(keywords) != null){
                    txtBuscar.setText("");
                }
            }
            
        }else{
            JOptionPane.showMessageDialog(null, "Debes escribir la clave del artículo o el nombre en el cuadro de busqueda");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

