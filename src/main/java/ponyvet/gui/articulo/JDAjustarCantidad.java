    private void ajustarExistencia(Float existencia) {

        if (articulo.isLote()) {
            float count = 0.0f;
            Set<Lote> lotes = articulo.getLotes();
            for (Lote lote : lotes) {
                count = count + lote.getExisActual();
            }
            if (existencia >= count) {
                if (existencia > 0 && existencia < ConfiguracionesGenerales.MAXIMA_CANTIDAD_EXISTENCIA) {
                    this.dispose();
                    articulo.setExistencia(existencia);
                    JDLotes jdlotes = new JDLotes(new JFrame(), true, articulo);
                    jdlotes.setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(null,
                            "La existencia debe ser mayor a 0 y menor a " + ConfiguracionesGenerales.MAXIMA_CANTIDAD_EXISTENCIA,
                            "Cantidad inválida", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(null,
                        "Debes agregar una cantiadad mayor al número de lotes existentes\n",
                        "Cantidad inválida", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            if (existencia >= 0 && existencia < ConfiguracionesGenerales.MAXIMA_CANTIDAD_EXISTENCIA) {
                this.dispose();
                ArticuloController.ajustarExistencia(articulo.getClave(), existencia);
                JFArticulo.actualizarTablaArticulos("");
            } else {
                JOptionPane.showMessageDialog(null,
                        "La existencia debe ser mayor o igual a 0 y menor a " + ConfiguracionesGenerales.MAXIMA_CANTIDAD_EXISTENCIA,
                        "Cantidad inválida", JOptionPane.ERROR_MESSAGE);
            }

        }
    }

