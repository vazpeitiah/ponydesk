    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        if (Validar.isFloat(txtCantidad.getText())) {
            Float cantidad = Float.parseFloat(txtCantidad.getText());

            if (articulo.isGranel()) { //Se vende a fracciones
                ajustarExistencia(cantidad);
            } else { //No se vende en fracciones
                if (cantidad == Math.round(cantidad)) { //verificar que se una entrada entera
                    ajustarExistencia(cantidad);
                } else {
                    JOptionPane.showMessageDialog(null,
                            "Este producto no se puede vender a granel(fracciones).\n"
                            + "Puedes configurarlo en la pestaña 'Adicional'.",
                            "Granel desactivado", JOptionPane.ERROR_MESSAGE);
                }
            }
        } else {
            JOptionPane.showMessageDialog(null,
                    "Cadena no válida. Debes introducir un número positivo entero o decimal",
                    "Advertencia", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

